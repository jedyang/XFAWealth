package com.fsll.wmes.role.vo;

import java.util.Date;

import com.fsll.wmes.entity.MemberRole;

public class MemberRoleVO {
	
	private String id;
	private String code;
	private String name;
	private String createBy;
	private Date createTime;
	private Date lastUpdate;
	private String createNickName;
	private String keyword;
	
	
	public MemberRoleVO() {
	}
	
	
	public MemberRoleVO(MemberRole role) {
		this.id = role.getId();
		this.code = role.getCode();
		this.name = role.getName();
		this.createBy = role.getCreateBy();
		this.createTime = role.getCreateTime();
		this.lastUpdate = role.getLastUpdate();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getLastUpdate() {
		return lastUpdate;
	}
	public void setLastUpdate(Date lastUpdate) {
		this.lastUpdate = lastUpdate;
	}
	public String getCreateNickName() {
		return createNickName;
	}
	public void setCreateNickName(String createNickName) {
		this.createNickName = createNickName;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	

}
