package com.fsll.wmes.investor.vo;

public class InvestorAccountCurrencyVO {
	private String memberId;
	private String accountNo;
	private double totalAssets = 0;
	private double marketValue = 0;
	private double cashValue = 0;
	private double cashAvailable = 0;
	private double cashHold = 0;
	private double cashWithdrawal = 0;
	private String currency;

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

	public String getAccountNo() {
		return accountNo;
	}

	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public double getTotalAssets() {
		return totalAssets;
	}

	public void setTotalAssets(double totalAssets) {
		this.totalAssets = totalAssets;
	}

	public double getMarketValue() {
		return marketValue;
	}

	public void setMarketValue(double marketValue) {
		this.marketValue = marketValue;
	}

	public double getCashValue() {
		return cashValue;
	}

	public void setCashValue(double cashValue) {
		this.cashValue = cashValue;
	}

	public double getCashAvailable() {
		return cashAvailable;
	}

	public void setCashAvailable(double cashAvailable) {
		this.cashAvailable = cashAvailable;
	}

	public double getCashHold() {
		return cashHold;
	}

	public void setCashHold(double cashHold) {
		this.cashHold = cashHold;
	}

	public double getCashWithdrawal() {
		return cashWithdrawal;
	}

	public void setCashWithdrawal(double cashWithdrawal) {
		this.cashWithdrawal = cashWithdrawal;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

}
