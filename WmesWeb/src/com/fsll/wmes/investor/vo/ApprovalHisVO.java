package com.fsll.wmes.investor.vo;

public class ApprovalHisVO {

	private String id;
	private String approveData;
	private String comment;
	private String action;
	private String flowStep;
	private String approver;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getApproveData() {
		return approveData;
	}
	public void setApproveData(String approveData) {
		this.approveData = approveData;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getFlowStep() {
		return flowStep;
	}
	public void setFlowStep(String flowStep) {
		this.flowStep = flowStep;
	}
	public String getApprover() {
		return approver;
	}
	public void setApprover(String approver) {
		this.approver = approver;
	}
	
	
	
}
