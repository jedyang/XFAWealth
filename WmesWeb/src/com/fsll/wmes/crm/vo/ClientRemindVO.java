package com.fsll.wmes.crm.vo;

public class ClientRemindVO {

	private String birthday="0";
	private String appoint="0";
	private String portfolioPre="0";
	private String kycPre="0";
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	
	public String getAppoint() {
		return appoint;
	}
	public void setAppoint(String appoint) {
		this.appoint = appoint;
	}
	public String getPortfolioPre() {
		return portfolioPre;
	}
	public void setPortfolioPre(String portfolioPre) {
		this.portfolioPre = portfolioPre;
	}
	public String getKycPre() {
		return kycPre;
	}
	public void setKycPre(String kycPre) {
		this.kycPre = kycPre;
	}
	
	
}
