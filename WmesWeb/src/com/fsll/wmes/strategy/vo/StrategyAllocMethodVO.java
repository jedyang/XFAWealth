package com.fsll.wmes.strategy.vo;

public class StrategyAllocMethodVO {

	private String typeName;
	private String methodType;
	private String methodTypeName;
	private String layerLevel;
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	public String getMethodType() {
		return methodType;
	}
	public void setMethodType(String methodType) {
		this.methodType = methodType;
	}
	
	public String getMethodTypeName() {
		return methodTypeName;
	}
	public void setMethodTypeName(String methodTypeName) {
		this.methodTypeName = methodTypeName;
	}
	public String getLayerLevel() {
		return layerLevel;
	}
	public void setLayerLevel(String layerLevel) {
		this.layerLevel = layerLevel;
	}
	
	
}
